package com.dinesh.typecasting;

public class Hello {
public static void main(String[] args) {
	short x = 10;
	byte y = (byte)x;
	System.out.println(y);

	double a = 2.98876557882d;
	float b = (float)a;
	System.out.println(b);
	
	
	double d = 2.99837765562d;
	int i = (int)d;
	System.out.println(i);

}
}
